    public void retornarEndereco(int cep) throws IOException, InterruptedException {

       
        
        
        ProcessarCep pc = new ProcessarCep();

        JSONObject json = pc.carregarEndereco(cep);
        
     
        logradouro.setText(json.getString("logradouro"));
        bairro.setText(json.getString("bairro"));
        complemento.setText(json.getString("complemento"));
        localidade.setText(json.getString("localidade"));
        ibge.setText(json.getString("ibge"));
        ddd.setText(json.getString("ddd"));
        uf.setText(json.getString("uf"));
        
    }

