    private void cepKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cepKeyTyped

        String caracteres = "0123456789";
        if (!caracteres.contains(evt.getKeyChar() + "")) {
            evt.consume();
        }
    }//GEN-LAST:event_cepKeyTyped

    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        if (cep.getText().replaceAll("[^0-9]", "").isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o campo com o cep");
            cep.requestFocus();
        } else {
            try {
                retornarEndereco(Integer.parseInt(cep.getText().replaceAll("[^0-9]", "")));
            } catch (InterruptedException ex) {
                Logger.getLogger(Buscador_cep.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Buscador_cep.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

